library IEEE;
use IEEE.STD_LOGIC_1164.all;
use IEEE.NUMERIC_STD.all;

entity TB_Decoder_5to32 is
    --  Port ( );
end entity TB_Decoder_5to32;

architecture RTL of TB_Decoder_5to32 is
    signal tb_input_i : std_logic_vector(4 downto 0);
    signal tb_output_o : std_logic_vector(31 downto 0);
begin
    DUT_DECODER5X32 : entity work.Decoder_5to32(RTL)
        port map(
            input_i => tb_input_i,
            output_o => tb_output_o
        );

    P_COMBINATIONAL : process
    begin
        ------------------------------------------------------------------
        ------------------------------------------------------------------
        ------------------------------------------------------------------
        tb_input_i <= "00000";
        wait for 2 ns;
        assert (to_integer(unsigned(tb_output_o)) = 1) report "TEST : 0 [FAILED]" severity warning;

        ------------------------------------------------------------------
        ------------------------------------------------------------------
        ------------------------------------------------------------------
        tb_input_i <= "00001";
        wait for 2 ns;
        assert (to_integer(unsigned(tb_output_o)) = 1*2) report "TEST : 1 [FAILED]" severity warning;

        ------------------------------------------------------------------
        ------------------------------------------------------------------
        ------------------------------------------------------------------
        tb_input_i <= "00010";
        wait for 2 ns;
        assert (to_integer(unsigned(tb_output_o)) = 2*2)  report "TEST : 2 [FAILED]" severity warning;

        ------------------------------------------------------------------
        ------------------------------------------------------------------
        ------------------------------------------------------------------
        tb_input_i <= "00011";
        wait for 2 ns;
        assert (to_integer(unsigned(tb_output_o)) = 4*2)  report "TEST : 3 [FAILED]" severity warning;

        ------------------------------------------------------------------
        ------------------------------------------------------------------
        ------------------------------------------------------------------
        tb_input_i <= "00100";
        wait for 2 ns;
        assert (to_integer(unsigned(tb_output_o)) = 8*2)  report "TEST : 4 [FAILED]" severity warning;

        ------------------------------------------------------------------
        ------------------------------------------------------------------
        ------------------------------------------------------------------
        tb_input_i <= "00101";
        wait for 2 ns;
        assert (to_integer(unsigned(tb_output_o)) = 16*2)  report "TEST : 5 [FAILED]" severity warning;

        ------------------------------------------------------------------
        ------------------------------------------------------------------
        ------------------------------------------------------------------
        tb_input_i <= "00110";
        wait for 2 ns;
        assert (to_integer(unsigned(tb_output_o)) = 32*2)  report "TEST : 6 [FAILED]" severity warning;

        ------------------------------------------------------------------
        ------------------------------------------------------------------
        ------------------------------------------------------------------
        tb_input_i <= "00111";
        wait for 2 ns;
        assert (to_integer(unsigned(tb_output_o)) = 64*2)  report "TEST : 7 [FAILED]" severity warning;

        ------------------------------------------------------------------
        ------------------------------------------------------------------
        ------------------------------------------------------------------
        tb_input_i <= "01000";
        wait for 2 ns;
        assert (to_integer(unsigned(tb_output_o)) = 128*2)  report "TEST : 8 [FAILED]" severity warning;

        ------------------------------------------------------------------
        ------------------------------------------------------------------
        ------------------------------------------------------------------
        tb_input_i <= "01001";
        wait for 2 ns;
        assert (to_integer(unsigned(tb_output_o)) = 256*2)  report "TEST : 9 [FAILED]" severity warning;

        ------------------------------------------------------------------
        ------------------------------------------------------------------
        ------------------------------------------------------------------
        tb_input_i <= "01010";
        wait for 2 ns;
        assert (to_integer(unsigned(tb_output_o)) = 512*2)  report "TEST : 10 [FAILED]" severity warning;


        ------------------------------------------------------------------
        ------------------------------------------------------------------
        ------------------------------------------------------------------
        tb_input_i <= "01011";
        wait for 2 ns;
        assert (to_integer(unsigned(tb_output_o)) = 1024*2)  report "TEST : 11 [FAILED]" severity warning;

        ------------------------------------------------------------------
        ------------------------------------------------------------------
        ------------------------------------------------------------------
        tb_input_i <= "01100";
        wait for 2 ns;
        assert (to_integer(unsigned(tb_output_o)) = 2048*2)  report "TEST : 12 [FAILED]" severity warning;

        ------------------------------------------------------------------
        ------------------------------------------------------------------
        ------------------------------------------------------------------
        tb_input_i <= "01101";
        wait for 2 ns;
        assert (to_integer(unsigned(tb_output_o)) = 2048*4)  report "TEST : 13 [FAILED]" severity warning;

                ------------------------------------------------------------------
        ------------------------------------------------------------------
        ------------------------------------------------------------------
        tb_input_i <= "01110";
        wait for 2 ns;
        assert (to_integer(unsigned(tb_output_o)) = 2048*8)  report "TEST : 14 [FAILED]" severity warning;

        ------------------------------------------------------------------
        ------------------------------------------------------------------
        ------------------------------------------------------------------
        tb_input_i <= "01111";
        wait for 2 ns;
        assert (to_integer(unsigned(tb_output_o)) = 2048*16)  report "TEST : 15 [FAILED]" severity warning;

        ------------------------------------------------------------------
        ------------------------------------------------------------------
        ------------------------------------------------------------------
        tb_input_i <= "11110";
        wait for 2 ns;
        assert (tb_output_o = x"40000000")  report "TEST : 16 [FAILED]" severity warning;

        ------------------------------------------------------------------
        ------------------------------------------------------------------
        ------------------------------------------------------------------
        tb_input_i <= "11111";
        wait for 2 ns;
        assert (tb_output_o = x"80000000")  report "TEST : 17 [FAILED]" severity warning;

        ------------------------------------------------------------------
        ------------------------------------------------------------------
        ------------------------------------------------------------------
        assert (false) report "ALL TESTS PASSED" severity failure;

    end process;

end architecture RTL;